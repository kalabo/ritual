        // GET: Administration
        public ActionResult Index()
        {
            //Redirect back to login page if not authenticated
            if (!HttpContext.User.Identity.IsAuthenticated)
            {
                return RedirectToAction("Login", "Account");
            }

            this.ApplicationDbContext = new ApplicationDbContext();
            this.UserManager = new UserManager<ApplicationUser>(new UserStore<ApplicationUser>(this.ApplicationDbContext));
            var user = UserManager.FindById(User.Identity.GetUserId());

            AdministrationDashboardData dashboardDataModel = new AdministrationDashboardData();

            var employee = db.Employees.Where(m => m.AspNetUserId == user.Id).Single();
            dashboardDataModel.RitualEmployee = employee;

            return View(dashboardDataModel);
        }

